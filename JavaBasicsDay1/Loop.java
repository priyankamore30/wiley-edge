class Loop{
    public static void main(String[] args){
        for(int i=0;i<4;i++){
            for(int j=0;j<3;j++){
                System.out.println("The value of i is "+i+" and j is "+j);
            }
        }

        System.out.println("The 10 numbers are :");
        while(i<=10){
            System.out.println(i);
        }
    }
}