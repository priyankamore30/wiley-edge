class Casting{
    public static void main(String [] args){
         float a = 234.4f;
         double b = a;
        // double b = (double)a;

        System.out.println(a);
        System.out.println(b);

        double c = 54.2d;
        int d = (int)c;
        System.out.println(c);
        System.out.println(d);
    }
}